--通讯录表
use grxx;

CREATE TABLE CONTACTS
(
   ID int NOT NULL IDENTITY (1, 1) PRIMARY KEY,
   NAME CHAR(10) NOT NULL,
   CONTACT CHAR(10),
   CADDRESS VARCHAR(50),
   CITY VARCHAR(20),
   COMMENT VARCHAR(100)
)

go

CREATE TABLE addressBook
(
   id int NOT NULL IDENTITY (1, 1) PRIMARY KEY,
   CONTACTSID INT NOT NULL,
   FOREIGN KEY (CONTACTSID) REFERENCES CONTACTS(ID) 
);

GO

CREATE TABLE memorandum
(
   id int NOT NULL IDENTITY (1, 1) PRIMARY KEY,
   MTIME DATE ,
   PLACE VARCHAR(50),   
   INCIDENT VARCHAR(100) NOT NULL,
   CONTACTSID INT ,
   FOREIGN KEY (CONTACTSID) REFERENCES CONTACTS(ID)
);

GO

CREATE TABLE diary
(
   id int NOT NULL IDENTITY (1, 1) PRIMARY KEY,
   --terminal --项目
   MTIME DATE,
   PLACE VARCHAR(50),   
   INCIDENT VARCHAR(100) NOT NULL,
   CONTACTSID INT ,
   FOREIGN KEY (CONTACTSID) REFERENCES CONTACTS(ID)
);

GO

CREATE TABLE property
(
   id int NOT NULL IDENTITY (1, 1) PRIMARY KEY,
   terminal VARCHAR(100),--项目   
   PMONEY MONEY NOT NULL,
   MTIME DATE,
   CONTACTSID INT ,
   FOREIGN KEY (CONTACTSID) REFERENCES CONTACTS(ID)
);
